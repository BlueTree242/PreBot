package me.bluetree242.prebot.spigot.logging;

import org.slf4j.ILoggerFactory;
import org.slf4j.IMarkerFactory;
import org.slf4j.helpers.BasicMDCAdapter;
import org.slf4j.helpers.BasicMarkerFactory;
import org.slf4j.spi.MDCAdapter;
import org.slf4j.spi.SLF4JServiceProvider;

public class LoggerProvider implements SLF4JServiceProvider {
    private final BasicMarkerFactory markerFactory = new BasicMarkerFactory();
    private final BasicMDCAdapter mdcAdapter = new BasicMDCAdapter();
    private final LoggerFactory factory = new LoggerFactory();
    @Override
    public ILoggerFactory getLoggerFactory() {
        return factory;
    }

    @Override
    public IMarkerFactory getMarkerFactory() {
        return markerFactory;
    }

    @Override
    public MDCAdapter getMDCAdapter() {
        return mdcAdapter;
    }

    @Override
    public String getRequestedApiVersion() {
        return "2.0.1";
    }

    @Override
    public void initialize() {

    }
}
